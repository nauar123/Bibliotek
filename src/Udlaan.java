public class Udlaan {


    private int maxUdlaan;
    private int udlaan = 0;


    public Udlaan()
    {}
    public Udlaan(int maxUd, int ud)
    {
        maxUdlaan = maxUd;
        udlaan = ud;
    }

    public int getMaxUdlaan()
    {
        return maxUdlaan;
    }

    public int getUdlaan()
    {
        return udlaan;
    }
}

